#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*newlist;

	newlist = (t_list *)malloc(sizeof(t_list));
	if (newlist == NULL)
		return (NULL);
	if (!content)
		newlist->content = NULL;
	else
		newlist->content = content;
	newlist->next = NULL;
	return (newlist);
}
