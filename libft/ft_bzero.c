#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	size_t	i;
	size_t	max;

	c = (char *)s;
	i = 0;
	max = n;
	while (i < max)
	{
		c[i] = '\0';
		i++;
	}
}
